; WRITE AN ALP USING ARM7TDMI TO MULTIPLICATION OF 16*31 WITHOUT USING MUL INSTRUCTIONS
; (HINT: BARREL SHIFT INSTRUCTIONS)

;CASE 1:31*16 (WHERE 16 IS CONSIDERED AS MULTIPLIER)
.TEXT
MOV R1,#31
MOV R2,R1,LSL #4   ;16*31=25*2POWER(4)=496
SWI 0X011

;CASE 2:16*31 (WHERE 31 IS CONSIDERED AS MULTIPLIER)
.TEXT
MOV R1,#16
RSB R3,R1,R1,LSL #5   ;16*2POWER(5)-1=496
SWI 0X011